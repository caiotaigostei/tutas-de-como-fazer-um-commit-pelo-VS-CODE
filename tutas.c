//Fazer commit no github pelo Vs Code:

//1)'git init' no terminal do Vs Code -> GIT INIT inicia um repositorio local
//2)'git add .' no terminal do Vs Code
//3)'git commit -m "comentário qualquer"' no terminal do Vs Code
//4)Depois com isso, siga para o github, crie um repositorio no seu perfil, com isso copie o link do seu repositorio recém criado
//5)'git remote add origin <link URL do seu recém repositorio>'
//6)'git branch -M main'no terminal do Vs Code
//7)'git push -u origin main'

//Com isso seu código estará salvo no github!

